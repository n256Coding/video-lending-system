    public void updateDetails(){
        DBConnect connect = new DBConnect();
        String query = "select * from videos where videoId ="+this.videoId;
        try{
            connect.rs = connect.st.executeQuery(query);
            connect.rs.next();
            jLabel_title.setText(connect.rs.getString("title"));
            jTextField_type.setText(connect.rs.getString("type"));
            jTextField_actor1.setText(connect.rs.getString("actor1"));
            jTextField_actor2.setText(connect.rs.getString("actor2"));
            jTextField_producer.setText(connect.rs.getString("producer"));
            jTextField_pPer7D.setText(connect.rs.getString("pricePer7d"));
            jTextField_pPerAddiD.setText(connect.rs.getString("addPricePerD"));
            jLabel_qty.setText(connect.rs.getString("qty")+" of Copies.");
            jLabel_videoId.setText(connect.rs.getString("videoId"));
            jTextArea_description.setText(connect.rs.getString("description"));
            jTextField_sellingPrice.setText(connect.rs.getString("price"));
            ImageIcon image = new ImageIcon(new ImageIcon(connect.rs.getString("artUrl")).getImage().getScaledInstance(211, 285, Image.SCALE_AREA_AVERAGING));
            jLabel_image.setIcon(image);
        }catch(Exception e){
            //JOptionPane.showMessageDialog(null, e);
            JOptionPane.showMessageDialog(null, "An Error Occured!!");
        }
    }
