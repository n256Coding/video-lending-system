    private void jTextField_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_searchKeyReleased
        DBConnect connect = new DBConnect();
        String keyword = jTextField_search.getText();
        String query = "select * from videos where title like '%"+keyword+"%'";
        DefaultListModel resultList =  (DefaultListModel) jList_searchList.getModel();
        resultList.removeAllElements();
        try{
            connect.rs = connect.st.executeQuery(query);
            while(connect.rs.next())
                resultList.addElement(connect.rs.getString("title"));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTextField_searchKeyReleased

    private void jList_searchListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList_searchListMouseClicked
        String title = jList_searchList.getSelectedValue().toString();
        title = title.trim();
        String query = "select videoId from videos where title = '"+title+"'";
        DBConnect connect = new DBConnect();
        try{
            connect.rs = connect.st.executeQuery(query);
            connect.rs.next();
            this.videoId = Integer.parseInt(connect.rs.getString("videoId").trim());
            updateDetails();
        }catch(Exception e){
        }
    }//GEN-LAST:event_jList_searchListMouseClicked

